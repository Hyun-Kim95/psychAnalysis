function detectDefaultLocale(): Locale {
  const stored = localStorage.getItem(STORAGE_KEY)
  if (stored === 'ko' || stored === 'en') return stored
  const language = navigator.language?.toLowerCase() ?? 'ko'
  return language.startsWith('en') ? 'en' : 'ko'
}

export const locale = ref<Locale>(detectDefaultLocale())

function syncDocumentLang(l: Locale) {
  if (typeof document !== 'undefined') {
    document.documentElement.lang = l === 'ko' ? 'ko' : 'en'
  }
}

syncDocumentLang(locale.value)

export function setLocale(next: Locale) {
  locale.value = next
  localStorage.setItem(STORAGE_KEY, next)
  syncDocumentLang(next)
}

export function t(key: string, params?: Record<string, string | number>): string {
  const template = messages[locale.value][key] ?? messages.ko[key] ?? key
  if (!params) return template
  return Object.entries(params).reduce(
    (acc, [paramKey, paramValue]) => acc.replaceAll(`{${paramKey}}`, String(paramValue)),
    template,
  )
}

export function useI18n() {
  return { locale, setLocale, t }
}
